int level2[mapHeight][mapWidth] = {  };

void Game::LoadLevel(int level)
{
	if (level == 1)
		memcpy(levelMap, level1, sizeof(levelMap));
	else if (level == 2)
		memcpy(levelMap, level2, sizeof(levelMap));
}
